"""Generate docs/index.html — the living project notebook (PT-BR study companion).

Curated prose lives in scripts/overview_content.py (edit that file to grow the doc).
Live facts (git timeline, file tree, Phase-1 task progress, stack versions) are read
from the repository here, so the notebook is always in sync with reality.

The HTML is a DERIVED artifact (gitignored): it is regenerated by the post-commit
hook and by the Pages CI workflow. See ADR-07 in the notebook.

Usage:
    python scripts/generate_overview.py        # or: uv run python scripts/generate_overview.py
"""

