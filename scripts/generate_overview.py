# --------------------------------------------------------------------------- #
# git / fs helpers
# --------------------------------------------------------------------------- #
def git(*args: str) -> str:
    try:
        r = subprocess.run(["git", *args], cwd=ROOT, capture_output=True, text=True, check=True)
        return r.stdout.strip()
    except Exception:
        return ""


def esc(s: object) -> str:
    """Escape values coming from git/fs. Curated content is trusted and NOT escaped."""
    return html.escape(str(s), quote=True)


def get_commits(limit: int = 50) -> list[dict]:
    sep = "\x1f"
    raw = git("log", f"-{limit}", f"--pretty=%h{sep}%ad{sep}%s", "--date=short")
    out: list[dict] = []
    for line in raw.splitlines():
        parts = line.split(sep)
        if len(parts) == 3:
            out.append({"sha": parts[0], "date": parts[1], "subject": parts[2]})
    return out


def count_total_commits() -> int:
    n = git("rev-list", "--count", "HEAD")
    return int(n) if n.isdigit() else 0


def count_source_modules() -> int:
    src = ROOT / "src"
    if not src.exists():
        return 0
    return sum(1 for p in src.rglob("*.py") if p.name != "__init__.py" and p.stat().st_size > 0)


def count_tests() -> int:
    tests = ROOT / "tests"
    if not tests.exists():
        return 0
    n = 0
    for p in tests.rglob("*.py"):
        try:
            text = p.read_text(encoding="utf-8")
        except Exception:
            continue
        n += sum(
            1
            for line in text.splitlines()
            if line.strip().startswith(("def test_", "async def test_"))
        )
    return n


def read_stack_versions() -> dict:
    pp = ROOT / "pyproject.toml"
    info: dict = {"runtime": [], "dev": [], "python": "—"}
    if not pp.exists():
        return info
    try:
        data = tomllib.loads(pp.read_text(encoding="utf-8"))
    except Exception:
        return info
    proj = data.get("project", {})
    info["python"] = proj.get("requires-python", "—")
    info["runtime"] = proj.get("dependencies", [])
    info["dev"] = data.get("dependency-groups", {}).get("dev", [])
    return info


def _task_done(t: dict) -> bool:
    """A task is done if it declares done=True or its key_file exists on disk."""
    if t.get("done"):
        return True
    kf = t.get("key_file")
    return bool(kf) and (ROOT / kf).exists()


def phases_progress() -> list[dict]:
    """Per-phase progress with auto-detected task completion."""
    phases: list[dict] = []
    for ph in C.PHASES:
        rows: list[dict] = []
        done = 0
        for t in ph["tasks"]:
            is_done = _task_done(t)
            done += int(is_done)
            rows.append({**t, "done": is_done, "manual": t.get("key_file") is None})
        phases.append(
            {
                "id": ph["id"],
                "name": ph["name"],
                "status": ph.get("status", "todo"),
                "summary": ph["summary"],
                "done": done,
                "total": len(ph["tasks"]),
                "rows": rows,
            }
        )
    return phases


def current_phase(phases: list[dict]) -> dict:
    """Phase to surface in the status bar: explicit 'current', else the first
    not-yet-complete one, else the last."""
    for ph in phases:
        if ph["status"] == "current":
            return ph
    for ph in phases:
        if ph["done"] < ph["total"]:
            return ph
    return phases[-1]


def build_tree() -> str:
    lines: list[str] = [f"{ROOT.name}/"]

    def walk(d: Path, prefix: str = "") -> None:
        try:
            entries = sorted(
                (
                    e
                    for e in d.iterdir()
                    if e.name not in IGNORE_DIRS
                    and not e.name.endswith(".pyc")
                    and e.name != "index.html"
                ),
                key=lambda e: (e.is_file(), e.name.lower()),
            )
        except Exception:
            return
        for i, e in enumerate(entries):
            last = i == len(entries) - 1
            lines.append(f"{prefix}{'└── ' if last else '├── '}{e.name}{'/' if e.is_dir() else ''}")
            if e.is_dir():
                walk(e, prefix + ("    " if last else "│   "))

    walk(ROOT)
    return "\n".join(lines)


# --------------------------------------------------------------------------- #
# render
# --------------------------------------------------------------------------- #
def section(id_: str, title: str, inner: str) -> str:
    return f'<section id="{id_}"><h2>{esc(title)}</h2>{inner}</section>'


def render_status(facts: dict) -> str:
    cur = current_phase(facts["phases"])
    done, total = cur["done"], cur["total"]
    pct = round(done / total * 100) if total else 0
    chips = [
        ("commit", facts["head"]),
        ("branch", facts["branch"]),
        ("commits", facts["total_commits"]),
        ("módulos", facts["modules"]),
        ("testes", facts["tests"]),
        ("python", facts["versions"]["python"]),
    ]
    chip_html = "".join(
        f'<span class="chip"><span class="chip-k">{esc(k)}</span>'
        f'<span class="chip-v">{esc(v)}</span></span>'
        for k, v in chips
    )
    return f"""
<header class="status">
  <div class="status-row">
    <div class="brand"><span class="dot"></span><strong>{esc(C.PROJECT["name"])}</strong>
      <span class="sub">caderno de bordo</span></div>
    <div class="chips">{chip_html}</div>
  </div>
  <div class="progress">
    <div class="progress-label">Fase {cur["id"]} · {done}/{total} tasks</div>
    <div class="bar"><div class="bar-fill" style="width:{pct}%"></div></div>
  </div>
</header>"""


